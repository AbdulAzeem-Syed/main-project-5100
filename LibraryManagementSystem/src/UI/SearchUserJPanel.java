        public void populateTable() {
        try {
            this.users = UserJDBConnector.getAllUsers();
            DefaultTableModel model = (DefaultTableModel) userTable.getModel();
            model.setRowCount(0);
            for (User u: users) {
                Object[] row = new Object[4];
                row[0] = u.getUserId();
                row[1] = u.getName();
                row[2] = u.getEmail();
                row[3] = u.getRole();
               
                model.addRow(row);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
